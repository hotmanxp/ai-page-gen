import { useState, useEffect } from 'react'
import { Row, Col, Card, Select, Button, Space, message, Spin, Switch } from 'antd'
import { MobileOutlined, DesktopOutlined, TabletOutlined, ShareAltOutlined, ApiOutlined } from '@ant-design/icons'
import PreviewArea from '../components/PreviewArea'
import ChatArea from '../components/ChatArea'
import PageSelector from '../components/PageSelector'
import socketService from '../services/socketService'
import { pageApi } from '../services/apiService'
import { useSearchParams } from 'react-router-dom'

const { Option } = Select

interface Message {
  id: string
  content: string
  sender: 'user' | 'ai'
  timestamp: Date
}

function HomePage() {
  const [searchParams] = useSearchParams()
  const [pageType, setPageType] = useState<'h5' | 'admin' | 'pc'>('h5')
  const [useLocalModel, setUseLocalModel] = useState<boolean>(true)
  const [messages, setMessages] = useState<Message[]>([])
  const [inputMessage, setInputMessage] = useState('')
  const [isGenerating, setIsGenerating] = useState(false)
  const [pageId, setPageId] = useState<string>('')
  const [pageContent, setPageContent] = useState<string>('')
  const [isLoadingPage, setIsLoadingPage] = useState(false)

  const handleSendMessage = async () => {
    if (!inputMessage.trim() || isGenerating) return

    const newMessage: Message = {
      id: Date.now().toString(),
      content: inputMessage,
      sender: 'user',
      timestamp: new Date()
    }

    setMessages(prev => [...prev, newMessage])
    setInputMessage('')
    setIsGenerating(true)

    try {
      // Á°Æ‰øùÈ°µÈù¢Â∑≤ÂàùÂßãÂåñ
      let currentPageId = pageId
      if (!currentPageId) {
        currentPageId = `page_${Date.now()}`
        setPageId(currentPageId)
        
        // Use the current user input as prompt for title generation
        const initializationResponse = await pageApi.initializePage(currentPageId, pageType, inputMessage)
        
        // Show the generated title in chat if available
        if (initializationResponse.title) {
          const titleMessage: Message = {
            id: `title_${Date.now()}`,
            content: `üìù È°µÈù¢Ê†áÈ¢ò: ${initializationResponse.title}`,
            sender: 'ai',
            timestamp: new Date()
          }
          setMessages(prev => [...prev, titleMessage])
        }
      }

      // Ê∑ªÂä†AIÊ≠£Âú®Â§ÑÁêÜÁöÑÊ∂àÊÅØ
      const processingMessage: Message = {
        id: (Date.now() + 1).toString(),
        content: 'Ê≠£Âú®Ë∞ÉÁî®AIÊúçÂä°ÁîüÊàêÈ°µÈù¢ÔºåËØ∑Á®çÂÄô...',
        sender: 'ai',
        timestamp: new Date()
      }
      setMessages(prev => [...prev, processingMessage])

      // Ë∞ÉÁî®AIÁîüÊàêÈ°µÈù¢ÔºàÂºÇÊ≠•Ôºâ
      const response = await pageApi.generatePage({
        pageId: currentPageId,
        pageType,
        userPrompt: inputMessage,
        useLocalModel: useLocalModel
      })

      if (response.status === 'processing') {
        // Êõ¥Êñ∞Â§ÑÁêÜ‰∏≠Ê∂àÊÅØ
        const processingUpdateMessage: Message = {
          id: (Date.now() + 2).toString(),
          content: 'È°µÈù¢ÁîüÊàê‰ªªÂä°Â∑≤Êèê‰∫§ÔºåÊ≠£Âú®ÂêéÂè∞Â§ÑÁêÜ‰∏≠...',
          sender: 'ai',
          timestamp: new Date()
        }
        setMessages(prev => [...prev.filter(msg => msg.id !== processingMessage.id), processingUpdateMessage])
      }
      
    } catch (error) {
      console.error('Êèê‰∫§ÁîüÊàê‰ªªÂä°Â§±Ë¥•:', error)
      message.error('Êèê‰∫§ÁîüÊàê‰ªªÂä°Â§±Ë¥•ÔºåËØ∑ÈáçËØï')
      
      const errorMessage: Message = {
        id: (Date.now() + 1).toString(),
        content: 'Êä±Ê≠âÔºåÊèê‰∫§ÁîüÊàê‰ªªÂä°Êó∂Âá∫Áé∞ÈîôËØØÔºåËØ∑ÈáçËØï„ÄÇ',
        sender: 'ai',
        timestamp: new Date()
      }
      setMessages(prev => [...prev, errorMessage])
      setIsGenerating(false)
    }
  }

  // WebSocketËøûÊé•ÁÆ°ÁêÜ
  useEffect(() => {
    if (pageId) {
      socketService.connect(pageId)
      
      // ÁõëÂê¨WebSocketÊ∂àÊÅØ
      const handleWebSocketMessage = (data: any) => {
        console.log('WebSocket message received:', data)
        
        switch (data.type) {
          case 'generation_start':
            setIsGenerating(true)
            const startMessage: Message = {
              id: `start_${Date.now()}`,
              content: 'AIÂºÄÂßãÁîüÊàêÈ°µÈù¢...',
              sender: 'ai',
              timestamp: new Date()
            }
            setMessages(prev => [...prev, startMessage])
            break
          case 'generation_complete':
            setIsGenerating(false)
            message.success('È°µÈù¢ÁîüÊàêÂÆåÊàêÔºÅ')
            const completeMessage: Message = {
              id: `complete_${Date.now()}`,
              content: '‚úÖ È°µÈù¢ÁîüÊàêÂÆåÊàêÔºÅ',
              sender: 'ai',
              timestamp: new Date()
            }
            setMessages(prev => [...prev, completeMessage])
            break
          case 'page_update':
            if (data.data?.content) {
              setPageContent(data.data.content)
              const updateMessage: Message = {
                id: `update_${Date.now()}`,
                content: 'üîÑ È°µÈù¢ÂÜÖÂÆπÂ∑≤Êõ¥Êñ∞',
                sender: 'ai',
                timestamp: new Date()
              }
              setMessages(prev => [...prev, updateMessage])
            }
            break
          case 'error':
            setIsGenerating(false)
            message.error(data.message || 'ÁîüÊàêÈ°µÈù¢Êó∂Âá∫Èîô')
            const errorMessage: Message = {
              id: `error_${Date.now()}`,
              content: `‚ùå ${data.message || 'Êä±Ê≠âÔºåÁîüÊàêÈ°µÈù¢Êó∂Âá∫Áé∞ÈîôËØØ„ÄÇ'}`,
              sender: 'ai',
              timestamp: new Date()
            }
            setMessages(prev => [...prev, errorMessage])
            break
        }
      }
      
      socketService.onPageMessage(handleWebSocketMessage)
      
      return () => {
        socketService.offPageMessage(handleWebSocketMessage)
        socketService.disconnect()
      }
    }
  }, [pageId])

  // Fetch existing page content when pageId is provided in URL
  const fetchExistingPage = async (existingPageId: string) => {
    if (!existingPageId) return
    
    setIsLoadingPage(true)
    try {
      const response = await pageApi.getPageContent(existingPageId)
      if (response.success && response.content) {
        setPageContent(response.content)
        setPageId(existingPageId)
        
        // Add a message to indicate page was loaded
        const loadMessage: Message = {
          id: `load_${Date.now()}`,
          content: `‚úÖ Â∑≤Âä†ËΩΩÈ°µÈù¢: ${existingPageId}`,
          sender: 'ai',
          timestamp: new Date()
        }
        setMessages(prev => [...prev, loadMessage])
      }
    } catch (error) {
      console.error('Failed to fetch existing page:', error)
      message.error('Ëé∑ÂèñÈ°µÈù¢ÂÜÖÂÆπÂ§±Ë¥•')
      
      // Fallback: generate new page ID
      const newPageId = `page_${Date.now()}`
      setPageId(newPageId)
    } finally {
      setIsLoadingPage(false)
    }
  }

  // Handle URL parameter on component mount
  useEffect(() => {
    const urlPageId = searchParams.get('pageId')
    if (urlPageId) {
      fetchExistingPage(urlPageId)
    } else {
      // Generate new page ID if none provided
      const newPageId = `page_${Date.now()}`
      setPageId(newPageId)
    }
  }, [])

  const getPageTypeIcon = () => {
    switch (pageType) {
      case 'h5':
        return <MobileOutlined />
      case 'admin':
        return <DesktopOutlined />
      case 'pc':
        return <TabletOutlined />
      default:
        return <MobileOutlined />
    }
  }

  const handleSharePage = () => {
    if (!pageId) {
      message.warning('ËØ∑ÂÖàÁîüÊàê‰∏Ä‰∏™È°µÈù¢')
      return
    }
    
    const shareUrl = `${window.location.origin}${window.location.pathname}?pageId=${pageId}`
    
    // Copy to clipboard
    navigator.clipboard.writeText(shareUrl).then(() => {
      message.success('ÂàÜ‰∫´ÈìæÊé•Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø')
    }).catch(() => {
      // Fallback for older browsers
      const textArea = document.createElement('textarea')
      textArea.value = shareUrl
      document.body.appendChild(textArea)
      textArea.select()
      document.execCommand('copy')
      document.body.removeChild(textArea)
      message.success('ÂàÜ‰∫´ÈìæÊé•Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø')
    })
  }

  const handlePageSelect = async (selectedPageId: string) => {
    setIsLoadingPage(true)
    try {
      const response = await pageApi.getPageContent(selectedPageId)
      if (response.success && response.content) {
        setPageContent(response.content)
        setPageId(selectedPageId)
        
        // Update URL without page reload
        const newUrl = `${window.location.pathname}?pageId=${selectedPageId}`
        window.history.pushState({}, '', newUrl)
        
        // Add a message to indicate page was loaded
        const loadMessage: Message = {
          id: `load_${Date.now()}`,
          content: `‚úÖ Â∑≤Âä†ËΩΩÈ°µÈù¢: ${selectedPageId}`,
          sender: 'ai',
          timestamp: new Date()
        }
        setMessages(prev => [...prev, loadMessage])
        message.success('È°µÈù¢Âä†ËΩΩÊàêÂäü')
      }
    } catch (error) {
      console.error('Failed to load selected page:', error)
      message.error('Âä†ËΩΩÈ°µÈù¢Â§±Ë¥•')
    } finally {
      setIsLoadingPage(false)
    }
  }

  return (
    <div className="h-full p-6">
      <Row gutter={[16, 16]} className="h-full">
        <Col span={16} className="h-full">
          <Card 
            title={
              <Space>
                {getPageTypeIcon()}
                <span>È°µÈù¢È¢ÑËßà</span>
              </Space>
            }
            className="h-full shadow-sm"
            extra={
              <Space>
                <PageSelector 
                  onPageSelect={handlePageSelect}
                  currentPageId={pageId}
                  currentPageType={pageType}
                />
                <Button 
                  icon={<ShareAltOutlined />} 
                  onClick={handleSharePage}
                  disabled={!pageId || isLoadingPage}
                >
                  ÂàÜ‰∫´
                </Button>
                <Space>
                  <span>Êú¨Âú∞Ê®°Âûã:</span>
                  <Switch
                    checked={useLocalModel}
                    onChange={setUseLocalModel}
                    checkedChildren={<ApiOutlined />}
                    unCheckedChildren="LM"
                  />
                </Space>
                <Select 
                  value={pageType} 
                  onChange={setPageType}
                  style={{ width: 120 }}
                >
                  <Option value="h5">H5ÁßªÂä®Á´Ø</Option>
                  <Option value="admin">ÁÆ°ÁêÜÁ´Ø</Option>
                  <Option value="pc">PCÁ´Ø</Option>
                </Select>
              </Space>
            }
          >
            {isLoadingPage ? (
              <div className="h-full flex items-center justify-center">
                <Spin size="large" tip="Ê≠£Âú®Âä†ËΩΩÈ°µÈù¢ÂÜÖÂÆπ..." />
              </div>
            ) : (
              <PreviewArea pageType={pageType} pageContent={pageContent} />
            )}
          </Card>
        </Col>
        <Col span={8} className="h-full">
          <Card 
            title="AIÂØπËØùÂå∫" 
            className="h-full shadow-sm"
          >
            <ChatArea 
              messages={messages}
              inputMessage={inputMessage}
              setInputMessage={setInputMessage}
              handleSendMessage={handleSendMessage}
              isGenerating={isGenerating}
            />
          </Card>
        </Col>
      </Row>
    </div>
  )
}

export default HomePage